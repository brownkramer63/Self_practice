import java.util.ArrayList;
import java.util.Arrays;
import java.util.List;

public class PerfectSquarePatch {
    public static void main(String[] args) {
        int n =5;

    }
    public static int[][] squarePatch(int n) {

        int[][] arr = new int[n][n];
        for (int i = 0; i <n ; i++) {
            for (int j = 0; j <n ; j++) {
                arr[i][j]=n;
            }
        }
return arr;
    }
}
